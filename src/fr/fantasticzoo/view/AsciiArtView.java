    private String getAsciiArtForAnimal(Creature creature) {
        if (creature instanceof Werewolf) {
            return getAsciiWerewolf();
        }
        if (creature instanceof Mermaids) {
            return getAsciiMermaid();
        }
        if (creature instanceof Dragons) {
            return getAsciiDragon();
        }
        if(creature instanceof Kraken) {
            return getAsciiKraken();
        }
        if(creature instanceof Megalodons) {
            return getAsciiMegalodon();
        }
        if(creature instanceof Nymphs) {
            return getAsciiNymph();
        }
        if(creature instanceof Phoenix) {
            return getPhoenixAscii();
        }
        return "null";
    }

